 #Peça ao usuário para inserir uma temperatura em graus Celsius.
celcius = float(input("Digite a temperatura em graus Celsius: "))
# Imprima a temperatura em graus Fahrenheit.
fahrenheit = (celcius * 9/5) + 32
print("A temperatura em Fahrenheit é: ", fahrenheit)    # Imprima a temperatura em graus Kelvin.
kelvin = celcius + 273.15   # Imprima a temperatura em graus Kelvin.
print("A temperatura em Kelvin é: ", kelvin)