import math  # Importa a biblioteca matemática para usar funções avançadas

# A função math.floor() arredonda um número para o menor inteiro mais próximo (arredondamento para baixo)
print(math.floor(2.5))  # Resultado: 2 (arredonda para baixo)
print(math.floor(2.3))  # Resultado: 2 (arredonda para baixo)
print(math.floor(8.66666))  # Resultado: 2 (arredonda para baixo)

import math  # Importa a biblioteca matemática para usar funções avançadas

# A função math.ceil() arredonda um número para o maior inteiro mais próximo (arredondamento para cima)
print(math.ceil(8.222))  # Resultado: 9 (arredonda para cima)
print(math.ceil(5.88))   # Resultado: 6 (arredonda para cima)
print(math.ceil(4.1))    # Resultado: 5 (arredonda para cima)


# A função math.sqrt() retorna a raiz quadrada de um número
print(math.sqrt(9))   # Resultado: 3.0 (raiz quadrada de 9)
print(math.sqrt(16))  # Resultado: 4.0 (raiz quadrada de 16)
print(math.sqrt(24))  # Resultado: 4.898... (raiz quadrada de 24, número decimal)










